          userId: userId,
          deliveryUserId: deliveryUserId,
          setUserId: setUserId,
          addressId: addressId,
          orderId: orderId,
          examId: examId,
          requestId: requestId,
          zoneId: zoneId,
    preOrderId: preOrderId,
           id: id,
          status: status??0,
    creator: creator,
          description: description,
    deliveryDate: deliveryDate,
    createdOn: createdOn,
    address: address,
    vatNumber: vatNumber,
    phoneNumber: phoneNumber,
    driver: driver,
    driverPhone: driverPhone,
    latitude: latitude,
    longitude: longitude,
        );

  factory DriverDeliveryModel.fromJson(Map<String, dynamic>? json) =>
      DriverDeliveryModel(
        userId: asT<String>(json, 'userId'),
        deliveryUserId: asT<String>(json, 'deliveryUserId'),
        setUserId: asT<String>(json, 'setUserId'),
        addressId: asT<int>(json, 'addressId'),
        orderId: asT<int>(json, 'orderId'),
        preOrderId: asT<int>(json, 'preOrderId'),
        examId: asT<int>(json, 'examId'),
        requestId: asT<int>(json, 'requestId'),
        id: asT<int>(json, 'id'),
        zoneId: asT<int>(json, 'zoneId'),
        status: asT<int>(json, 'status'),
        deliveryDate: asT<String>(json, 'deliveryDate'),
        description: asT<String>(json, 'description'),
        creator: asT<String>(json, 'creator'),
        createdOn: asT<String>(json, 'createdOn'),
        address: asT<String>(json, 'address'),
        phoneNumber: asT<String>(json, 'phoneNumber'),
        driver: asT<String>(json, 'dirver'),
        vatNumber: asT<String>(json, 'vatNumber'),
        driverPhone: asT<String>(json, 'dirverphone'),
        latitude: asT<double>(json, 'latitude'),
        longitude: asT<double>(json, 'longitude'),
      );

  Map<String, dynamic> toJson() => {
        'userId': userId,
        'deliveryUserId': deliveryUserId,
        'setUserId': setUserId,
        'addressId': addressId,
        'orderId': orderId,
        'examId': examId,
        'requestId': requestId,
        'preOrderId': preOrderId,
