        'phoneNumber': phoneNumber,
        'address': address,
        'vatNumber': vatNumber,
        'dirver': driver,
        'dirverphone': driverPhone,
        'latitude': latitude,
        'longitude': longitude,
        'id': id,
      };

  DriverDeliveryModel.castFromEntity(final DriverDeliveryEntity item)
      : super(
          userId: item.userId,
          deliveryUserId: item.deliveryUserId,
          setUserId: item.setUserId,
          addressId: item.addressId,
          orderId: item.orderId,
    preOrderId: item.preOrderId,
          examId: item.examId,
          requestId: item.requestId,
          zoneId: item.zoneId,
          status: item.status,
          description: item.description,
